let choise = Number(prompt('1-Americano,2-Espresso,3-Cappuccino   please enter you choise of coffee'));


switch(choise){
    case choise = 1:
        alert('Americano');
            break;
    
    case choise = 2:
        alert('Espresso');
            break;

    case choise = 3:
        alert('Cappuccino');
            break;
        
    default:{
        alert('Unknown');
    }
        
        
        

}

